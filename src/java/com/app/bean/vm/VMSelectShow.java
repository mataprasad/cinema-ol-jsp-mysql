package com.app.bean.vm;

public class VMSelectShow {
	private String pMovieTitle;
	private String pMovieDate;

	public VMSelectShow() {
		pMovieTitle = "";
		pMovieDate = "";
	}

	public String getpMovieTitle() {
		return pMovieTitle;
	}

	public void setpMovieTitle(String pMovieTitle) {
		this.pMovieTitle = pMovieTitle;
	}

	public String getpMovieDate() {
		return pMovieDate;
	}

	public void setpMovieDate(String pMovieDate) {
		this.pMovieDate = pMovieDate;
	}
}
