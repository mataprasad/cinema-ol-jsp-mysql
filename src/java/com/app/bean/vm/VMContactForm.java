package com.app.bean.vm;

public class VMContactForm {

    private String txtContact = "";
    private String txtAddress = "";
    private String txtContactNo = "";
    private String txtEmail = "";
    private String txtViews = "";

    /**
     * @return the txtContact
     */
    public String getTxtContact() {
        return txtContact;
    }

    /**
     * @param txtContact the txtContact to set
     */
    public void setTxtContact(String txtContact) {
        this.txtContact = txtContact;
    }

    /**
     * @return the txtAddress
     */
    public String getTxtAddress() {
        return txtAddress;
    }

    /**
     * @param txtAddress the txtAddress to set
     */
    public void setTxtAddress(String txtAddress) {
        this.txtAddress = txtAddress;
    }

    /**
     * @return the txtContactNo
     */
    public String getTxtContactNo() {
        return txtContactNo;
    }

    /**
     * @param txtContactNo the txtContactNo to set
     */
    public void setTxtContactNo(String txtContactNo) {
        this.txtContactNo = txtContactNo;
    }

    /**
     * @return the txtEmail
     */
    public String getTxtEmail() {
        return txtEmail;
    }

    /**
     * @param txtEmail the txtEmail to set
     */
    public void setTxtEmail(String txtEmail) {
        this.txtEmail = txtEmail;
    }

    /**
     * @return the txtViews
     */
    public String getTxtViews() {
        return txtViews;
    }

    /**
     * @param txtViews the txtViews to set
     */
    public void setTxtViews(String txtViews) {
        this.txtViews = txtViews;
    }
}
