package com.app.bean.db;

public class ShowDetail {

    private String Show_Id = "";
    private String Show_Date = "";
    private String Show_StartTime = "";
    private String Movie_Name = "";
    private String Hall_No = "";

    public String getShow_Id() {
        return Show_Id;
    }

    public void setShow_Id(String Show_Id) {
        this.Show_Id = Show_Id;
    }

    public String getShow_Date() {
        return Show_Date;
    }

    public void setShow_Date(String Show_Date) {
        this.Show_Date = Show_Date;
    }

    public String getShow_StartTime() {
        return Show_StartTime;
    }

    public void setShow_StartTime(String Show_StartTime) {
        this.Show_StartTime = Show_StartTime;
    }

    public String getMovie_Name() {
        return Movie_Name;
    }

    public void setMovie_Name(String Movie_Name) {
        this.Movie_Name = Movie_Name;
    }

    public String getHall_No() {
        return Hall_No;
    }

    public void setHall_No(String Hall_No) {
        this.Hall_No = Hall_No;
    }
}
